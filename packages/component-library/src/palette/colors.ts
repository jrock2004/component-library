/**
 * Takes a color and a number which is a percentage of opacity
 * @param color string - hex color
 * @param percentage number - percentage of opacity
 * @returns string - new color hex
 */
export const getColorPercentage = (
  color: string,
  percentage: number,
): string => {
  const hex = color.match(/\w\w/g);
  const a = percentage / 100;

  if (hex) {
    const [r, g, b] = hex.map((x) => parseInt(x, 16));

    return `rgba(${r},${g},${b},${a})`;
  }

  return color;
};
